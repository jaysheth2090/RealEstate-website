<h7 allign="center">Sale of Residence - Real Estate Tax Tips</h7>

	
		
	<br />	
		
		
		
		<p>You may qualify to exclude from your income all or part of any gain from the sale of your main home. Your main home is the one in which you live most of the time.</p>

<h7>Ownership and Use Tests</h7>

<p>To claim the exclusion, you must meet the ownership and use tests. This means that during the 5-year period ending on the date of the sale, you must have:</p>

<ul>
<li class="first-child">Owned the home for at least&nbsp;two years (the ownership test)</li>

<li class="last-child">Lived in the home as your main home for at least&nbsp;two years (the use test)</li>
</ul>

<h7>Gain</h7>

<p>If you have a gain from the sale of your main home, you may be able to exclude up to $250,000 of the gain from your income ($500,000 on a joint return in most cases).</p>

<ul>
<li class="first-child">If you can exclude all of the gain, you do not need to report the sale on your tax return</li>

<li class="last-child">If you have gain that cannot be excluded, it is taxable. Report it on Schedule D (Form 1040)</li>
</ul>

<h7>Loss</h7>

<p>You cannot deduct a loss from the sale of your main home.</p>

<h7>Worksheets</h7>

<p>Worksheets are included in Publication 523, Selling Your Home, to help you figure the:</p>

<ul>
<li class="first-child">Adjusted basis of the home you sold</li>

<li>Gain (or loss) on the sale</li>

<li class="last-child">Gain that you can exclude</li>
</ul>

<h7>Reporting the Sale</h7>

<p>Do not report the sale of your main home on your tax return unless:</p>

<ul>
<li class="first-child">You have a gain and do not qualify to exclude all of it,</li>

<li>You have a gain and choose not to exclude it, or</li>

<li class="last-child">You&nbsp;have a loss and received a Form 1099-S.</li>
</ul>

<h7>More Than One Home</h7>

<p>If you have more than one home, you can exclude gain only from the sale of your main home. You must pay tax on the gain from selling any other home. If you have two homes and live in both of them, your main home is ordinarily the one you live in most of the time.</p>

<p><b>Example One:</b></p>

<p>You own and live in a house in the city. You also own a beach house, which you use during the summer months. The house in the city is your main home; the beach house is not.</p>

<p><b>Example Two:</b></p>

<p>You own a house, but you live in another house that you rent. The rented house is your main home.</p>

